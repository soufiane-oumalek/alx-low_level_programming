#include "3-calc.h"

/**
 * op_add - adds 2 int
 * @a: first int
 * @b: second int
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts 2 int
 * @a: first int
 * @b: second int
 * Return: the diffrence
 */
int op_sub(int a, int b)
{
	return ( a - b);
}

/**
 * op_mul - multiplication of 2 int
 * @a: first int
 * @b: second int
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of 2 int
 * @a: first int 
 * @b: second in
 * Return: the result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of 2 int
 * @a: first int
 * @b: second int
 * Return: the remainder of the division
 */
int op_mod(int a, int b)
{
	return (a % b);
}

